package com.diablo.asssgn113;

public class Legs {

	String size;
	String width;
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public String toString(){
		return "has leg size "+size+" and width "+width;
		
	}
	
}
